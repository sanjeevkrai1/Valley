package com.valley.cws.bean;


public class ValidateReqBean {
	
	private String lastOtp;
	private String macAddress;
	private String type;
	private String checkMobile;
	
	public String getLastOtp() {
		return lastOtp;
	}
	public void setLastOtp(String lastOtp) {
		this.lastOtp = lastOtp;
	}
	public String getMacAddress() {
		return macAddress;
	}
	public void setMacAddress(String macAddress) {
		this.macAddress = macAddress;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCheckMobile() {
		return checkMobile;
	}
	public void setCheckMobile(String checkMobile) {
		this.checkMobile = checkMobile;
	}
	
	
	

}
